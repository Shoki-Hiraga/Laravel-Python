人身傷害（保険金額/1名につき）：3,000万円
入院諸費用特約：なし
車両保険：なし

上記でシミュレーションしたところ、約5万円/年でした。

注意したいポイントは、車両保険がつけられない点です。車両保険をつけることができれば、万が一の事故の際に一定の金額まで修理代金が保証されます。しかし、古いクルマのだと車両保険を申し込みできないケースが非常に多いです。車両保険を付帯できないと、事故発生時の修理代金を全額自分で負担しなければなりません。

