<h2><span id="outline_0">R32型 スカイライン GT-Rの維持費の内訳</span></h2>
<p><img src="https://assets.qsha-oh.com/img/post/7c87ce33-ac21-4a73-9a8f-22d67f77ba3c" alt="" width="1200" height="1200"></p>
<p>R32型 スカイライン GT-Rの維持費について、燃料代、自動車税種別割、任意保険料、車検代、メンテナンス費用の5項目に分けて解説します。</p>
<h3>燃料代</h3>
<p>R32型 スカイライン GT-Rのカタログ燃費は年式によって異なり、7〜8.2km/Lです。今回は8.2km/Lで燃料代を算出します。</p>
<p>たとえば、毎週末2時間ほどドライブした場合、走行距離はおおよそ400km程度まで伸び、約48Lの燃料を使用します。ハイオクガソリン1Lあたり184円（※）だとすると、1ヶ月あたりの燃料代は8,832円で、<span class="highlight">1年間に換算すると10万5,984円</span>かかります。</p>
<p>※2024年7月11日時点のハイオクガソリン1Lあたりの価格</p>
<h3>自動車税</h3>
<p>続いて、自動車税種別割について解説します。</p>
<p>自動車税種別割は年に1回納める税金で、クルマの排気量で税額が決まります。R32型 スカイライン GT-Rには、日産の名機である「RB26DETT型エンジン」が搭載されており、排気量は2,568ccです。2,500cc超〜3,000cc以下の区分に該当し、年間で5万1,000円支払うことになります。（2019年9月30日以前に新規登録した場合）</p>
<p>しかし、新規登録から13年以上経過していると税額が高くなります。R32型 スカイライン GT-Rの最終モデルが発売されたのは1994年です。どんなに年式が新しい個体でも車齢は13年を超えます。そのため、自動車税種別割は重課されて<span class="highlight">年間で5万8,600円</span>かかります。</p>
<h3>任意保険</h3>
<p>R32型 スカイライン GT-Rの任意保険料について大手のネット型保険で見積もりをしました。</p>
<p>＜条件＞<br />年齢：25歳<br />等級：6S<br />使用目的：日常・レジャー<br />運転者：本人限定<br />クルマの年式：1993年</p>
<p>＜補償内容＞<br />対人賠償（1名につき）：無制限<br />対物賠償（1事故につき）：無制限<br />人身傷害：あり（車内のみ補償）<br />人身傷害（保険金額/1名につき）：3,000万円<br />入院諸費用特約：なし<br />車両保険：なし</p>
<p>上記でシミュレーションしたところ、<span class="highlight">約5万円/年</span>でした。</p>
<p>注意したいポイントは、車両保険がつけられない点です。車両保険をつけることができれば、万が一の事故の際に一定の金額まで修理代金が保証されます。しかし、古いクルマのだと車両保険を申し込みできないケースが非常に多いです。車両保険を付帯できないと、事故発生時の修理代金を全額自分で負担しなければなりません。</p>
<p>R32型 スカイライン GT-Rは、高性能エンジンを搭載するハイパフォーマンスモデルであるためにスポーツ走行を楽しむ方が多いですが、ボディを擦ったりバンパーが破損したりしてしまうと10万円以上の修理代金がかかる可能性があります。さらに大きな事故で車輌が致命的なダメージを負った場合には、100万円近い金額に上るかもしれません。</p>
<h3>車検</h3>
<p>続いて、スカイライン R32  GT-Rの車検代について解説します。</p>
<p>＜ディーラー車検の場合＞<br />自賠責保険：1万7,650円（24ヶ月）<br />自動車重量税：3万7,800円（24ヶ月）※初年度登録から18年経過で算出<br />印紙代：1,800円<br />車検料：4万円<br /><span class="highlight">合計：9万7,250円</span></p>
<p>自賠責保険は「強制保険」とも呼ばれる、加入が義務付けられている保険です。自家用の乗用車の保険料は一律で1万7,650円です。（2024年7月時点）</p>
<p>自動車重量税は、車輌重量によって税額が変わります。R32型 スカイライン GT-Rは、1t超～1.5t以下の区分に該当し税額は2万4,600円です。しかし、自動車税種別割と同様に古いクルマの場合は重課されます。新規登録から18年以上経過していると、2段階の重課によって3万7,800円まで税額が上がります。</p>
<h3>メンテナンス費用</h3>
<p>次に、メンテナンス費用を紹介します。基本的なメンテナンス箇所とR32型 スカイライン GT−R特有の故障箇所、それぞれにかかる費用について解説します。</p>
<h4>基本的なメンテナンス箇所</h4>
<p>R32型 スカイライン GT-Rだけではなく、ほとんどのクルマでメンテナンスが必要といわれている項目は以下のとおりです。一般的に<span class="highlight">年間5万円ほど</span>かかります。</p>
<p>・洗車<br />・ワイパーゴム交換<br />・ウォッシャー液交換<br />・冷却水補充<br />・エアコンフィルター交換<br />・ヘッドライト交換<br />・エンジンオイル交換<br />・オイルフィルター交換<br />・ブレーキオイル交換<br />・エアクリーナー交換　など</p>
<h4>R32型 スカイライン GT-Rで故障しやすい箇所</h4>
<p>R32型 スカイライン GT-Rは発売から30年以上経過しているうえにスポーツ走行でダメージが蓄積している個体が多い傾向にあります。そのため、一般的なクルマよりもこまめに、半年に一度くらいは工場やショップでメンテナンスしてもらうほうがよいがよいでしょう。</p>
<p>R32型 スカイライン GT-Rで故障しやすいといわれている箇所は以下のとおりです。</p>
<p>・パワステオイルの漏れ<br />・アッパーアームの故障<br />・バッテリーターミナルの劣化<br />・タイミングベルトの劣化<br />・燃料ポンプのモジュレーターの腐食　など</p>
<p>上記はあくまで一例で、車輌の状態によってはさらに修理箇所が増える場合があります。費用については、<span class="highlight">多く見積もって30万円程度</span>と考えておくとよいでしょう。</p>
<h4>【知っておきたい】R32型 スカイライン GT-Rは今後も長く乗ることができるのか？</h4>
